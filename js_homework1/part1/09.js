function calculateAverage(dict) {
    let sum = 0
    for (let steps of Object.values(dict)) {
        sum += steps
    }
    let average = sum / Object.values(dict).length
    return average
}

// –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º —Ñ—É–Ω–∫—Ü–∏—é, –∫–æ—Ç–æ—Ä–∞—è –∞–Ω–∞–ª–∏–∑–∏—Ä—É–µ—Ç —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –∏ –≤—ã–≤–æ–¥–∏—Ç –æ–±—ä–µ–∫—Ç —Å –º–µ—Å—è—Ü–∞–º–∏ –∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ–º —à–∞–≥–æ–≤, –≤ –∫–æ—Ç–æ—Ä—ã—Ö —Ü–µ–ª—å –±—ã–ª–∞ –¥–æ—Å—Ç–∏–≥–Ω—É—Ç–∞
function analyzeActivity(dict) {
    emptyObj = {}
    for (let [month, steps] of Object.entries(dict)) {
        if (steps > stepsGoal) {
            emptyObj[month] = steps
        }
    }
    return emptyObj
}

